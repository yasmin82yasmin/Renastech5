package operatorpractice;

public class UnaryIncrementDecrement {
    public static void main(String[] args) {


            int i;
            i = 5;

            System.out.println(i);
            i++;//6
            System.out.println(i);

            ++i;

            System.out.println(i);
            i--;
            System.out.println(i);
            --i;
            System.out.println(i);


    }
}
