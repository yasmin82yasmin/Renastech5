package fundamentals;

public class FloatinPointDataTypes {
    public static void main(String[] args) {


            // float   f   =    5.5    ;
            float temperature = 80.5f ;
            System.out.println(temperature);
            temperature = 75.6F;
            System.out.println(temperature);
            double d = 8945789.58978 ;
            System.out.println(d);
            d = 69.6d;
            System.out.println(d);
            d = 9.6D;
            System.out.println(d);
            double val = 56.5f ;
    }
}
